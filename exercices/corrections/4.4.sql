-- 1. Séquence sur toute les tables
-- SEQ Table CATEGORIE
CREATE SEQUENCE SEQ_CATEGORIE
START WITH 9
INCREMENT BY 1;
-- PostgreSQL
ALTER TABLE CATEGORIES
ALTER COLUMN code_categorie SET DEFAULT nextval('SEQ_CATEGORIE');
-- ORACLE
ALTER TABLE CATEGORIES MODIFY CODE_CATEGORIE DEFAULT SEQ_CATEGORIE.NEXTVAL;
-- MariaDB
ALTER TABLE CATEGORIES ALTER COLUMN CODE_CATEGORIE SET DEFAULT NEXT VALUE FOR SEQ_CATEGORIE;
-- SQL Server
ALTER TABLE CATEGORIES ADD DEFAULT NEXT VALUE FOR SEQ_CATEGORIE FOR CODE_CATEGORIE;

