-- 2. Nouvelles tables CLIENTS_V2 et FOURNISSEURS_V2 à partir des tables CLIENTS et FOURNISSEURS
CREATE TABLE CLIENTS_V2 AS
SELECT societe, adresse, ville, code_postal, pays, telephone, fax
FROM CLIENTS;
CREATE TABLE FOURNISSEURS_V2 AS
SELECT societe, adresse, ville, code_postal, pays, telephone, fax
FROM FOURNISSEURS;

-- 3. Séquence commune entre CLIENTS_V2 et FOURNISSEURS_V2
CREATE SEQUENCE SEQ_PARTENAIRE
START WITH 1
INCREMENT BY 1;
-- Les modifications de la structure selon les SGBDR
-- PostgreSQL
ALTER TABLE FOURNISSEURS_V2
ADD COLUMN no_fournisseur INT PRIMARY KEY
DEFAULT nextval('SEQ_PARTENAIRE');
ALTER TABLE CLIENTS_V2
ADD COLUMN code_client INT PRIMARY KEY
DEFAULT nextval('SEQ_PARTENAIRE');
--  SQL SERVER
ALTER TABLE FOURNISSEURS_V2 ADD 
  NO_FOURNISSEUR int DEFAULT NEXT VALUE FOR SEQ_PARTENAIRE,
  PRIMARY KEY (NO_FOURNISSEUR)
;
ALTER TABLE CLIENTS_V2 ADD 
  CODE_CLIENT int DEFAULT NEXT VALUE FOR SEQ_PARTENAIRE,
  PRIMARY KEY (CODE_CLIENT)
;
-- ORACLE
ALTER TABLE FOURNISSEURS_V2 ADD (
  NO_FOURNISSEUR number(10) DEFAULT SEQ_PARTENAIRE.NEXTVAL,
  PRIMARY KEY (NO_FOURNISSEUR)
);
ALTER TABLE CLIENTS_V2 ADD (
  CODE_CLIENT number(10) DEFAULT SEQ_PARTENAIRE.NEXTVAL,
  PRIMARY KEY (CODE_CLIENT)
);
-- MariaDB
ALTER TABLE FOURNISSEURS_V2 ADD NO_FOURNISSEUR int DEFAULT NEXT VALUE FOR SEQ_PARTENAIRE,
ADD PRIMARY KEY (NO_FOURNISSEUR);
ALTER TABLE CLIENTS_V2 ADD CODE_CLIENT int DEFAULT NEXT VALUE FOR SEQ_PARTENAIRE,
ADD PRIMARY KEY (CODE_CLIENT);